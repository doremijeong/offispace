package kr.co.offispace.dto;

import java.util.Date;

public class ReplyVO {
	private String replyCode;
	private String replyWrite;
	private String replyContent;
	private Date replyWriteDt;
	private Date replyModifyDt;
	private String boardCode;

	private EmployeeVO replyEmp;

	public String getReplyCode() {
		return replyCode;
	}

	public void setReplyCode(String replyCode) {
		this.replyCode = replyCode;
	}

	public String getReplyWrite() {
		return replyWrite;
	}

	public void setReplyWrite(String replyWrite) {
		this.replyWrite = replyWrite;
	}

	public String getReplyContent() {
		return replyContent;
	}

	public void setReplyContent(String replyContent) {
		this.replyContent = replyContent;
	}

	public Date getReplyWriteDt() {
		return replyWriteDt;
	}

	public void setReplyWriteDt(Date replyWriteDt) {
		this.replyWriteDt = replyWriteDt;
	}

	public Date getReplyModifyDt() {
		return replyModifyDt;
	}

	public void setReplyModifyDt(Date replyModifyDt) {
		this.replyModifyDt = replyModifyDt;
	}

	public String getBoardCode() {
		return boardCode;
	}

	public void setBoardCode(String boardCode) {
		this.boardCode = boardCode;
	}

	public EmployeeVO getReplyEmp() {
		return replyEmp;
	}

	public void setReplyEmp(EmployeeVO replyEmp) {
		this.replyEmp = replyEmp;
	}


}
