package kr.co.offispace.dto;

import java.util.Date;

public class ApvAgencyVO {
	private String empCode;
	private String agenReason;
	private String giveEmpCode;
	private Date agenStartDt;
	private Date agenEndDt;

	private EmployeeVO fromEmp; // 대결자 지정을 한 사람
	private EmployeeVO toEmp; // 대결자로 지정된 사람

	public Date getAgenStartDt() {
		return agenStartDt;
	}

	public void setAgenStartDt(Date agenStartDt) {
		this.agenStartDt = agenStartDt;
	}

	public Date getAgenEndDt() {
		return agenEndDt;
	}

	public void setAgenEndDt(Date agenEndDt) {
		this.agenEndDt = agenEndDt;
	}

	public EmployeeVO getFromEmp() {
		return fromEmp;
	}

	public void setFromEmp(EmployeeVO fromEmp) {
		this.fromEmp = fromEmp;
	}

	public EmployeeVO getToEmp() {
		return toEmp;
	}

	public void setToEmp(EmployeeVO toEmp) {
		this.toEmp = toEmp;
	}

	public String getEmpCode() {
		return empCode;
	}

	public void setEmpCode(String empCode) {
		this.empCode = empCode;
	}

	public String getAgenReason() {
		return agenReason;
	}

	public void setAgenReason(String agenReason) {
		this.agenReason = agenReason;
	}

	public String getGiveEmpCode() {
		return giveEmpCode;
	}

	public void setGiveEmpCode(String giveEmpCode) {
		this.giveEmpCode = giveEmpCode;
	}

}
