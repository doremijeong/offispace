package kr.co.offispace.dto;

import java.util.Date;
import java.util.List;

public class NoticeVO {

	private String noticeCode;
	private String noticeTitle;
	private String noticeContent;
	private String noticeWriter;
	private Date noticeDate;
	private Date noticeUpdate;
	private Date noticeSdate;
	private Date noticeEdate;
	private String noticeImportance;
	private int noticeViewcnt;

	// 작성자 empVO
	private EmployeeVO writerEmp;

	// 첨부파일 리스트
	private List<AttachmentVO> attachList;

	public int getNoticeViewcnt() {
		return noticeViewcnt;
	}

	public void setNoticeViewcnt(int noticeViewcnt) {
		this.noticeViewcnt = noticeViewcnt;
	}

	public String getNoticeCode() {
		return noticeCode;
	}

	public void setNoticeCode(String noticeCode) {
		this.noticeCode = noticeCode;
	}

	public String getNoticeTitle() {
		return noticeTitle;
	}

	public void setNoticeTitle(String noticeTitle) {
		this.noticeTitle = noticeTitle;
	}

	public String getNoticeContent() {
		return noticeContent;
	}

	public void setNoticeContent(String noticeContent) {
		this.noticeContent = noticeContent;
	}

	public String getNoticeWriter() {
		return noticeWriter;
	}

	public void setNoticeWriter(String noticeWriter) {
		this.noticeWriter = noticeWriter;
	}

	public Date getNoticeDate() {
		return noticeDate;
	}

	public void setNoticeDate(Date noticeDate) {
		this.noticeDate = noticeDate;
	}

	public Date getNoticeUpdate() {
		return noticeUpdate;
	}

	public void setNoticeUpdate(Date noticeUpdate) {
		this.noticeUpdate = noticeUpdate;
	}

	public Date getNoticeSdate() {
		return noticeSdate;
	}

	public void setNoticeSdate(Date noticeSdate) {
		this.noticeSdate = noticeSdate;
	}

	public Date getNoticeEdate() {
		return noticeEdate;
	}

	public void setNoticeEdate(Date noticeEdate) {
		this.noticeEdate = noticeEdate;
	}

	public String getNoticeImportance() {
		return noticeImportance;
	}

	public void setNoticeImportance(String noticeImportance) {
		this.noticeImportance = noticeImportance;
	}

	public EmployeeVO getWriterEmp() {
		return writerEmp;
	}

	public void setWriterEmp(EmployeeVO writerEmp) {
		this.writerEmp = writerEmp;
	}

	public List<AttachmentVO> getAttachList() {
		return attachList;
	}

	public void setAttachList(List<AttachmentVO> attachList) {
		this.attachList = attachList;
	}

}
