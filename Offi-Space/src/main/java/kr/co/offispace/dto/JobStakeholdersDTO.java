package kr.co.offispace.dto;

import java.util.List;

public class JobStakeholdersDTO {

	private String jobcodeCode;
	private String jobcodeName;
	private EmployeeVO jobCharger;
	private String jobTeamCode;
	private List<EmployeeVO> jobAssistants;
	
	public String getJobcodeCode() {
		return jobcodeCode;
	}
	public void setJobcodeCode(String jobcodeCode) {
		this.jobcodeCode = jobcodeCode;
	}
	public String getJobcodeName() {
		return jobcodeName;
	}
	public void setJobcodeName(String jobcodeName) {
		this.jobcodeName = jobcodeName;
	}
	public EmployeeVO getJobCharger() {
		return jobCharger;
	}
	public void setJobCharger(EmployeeVO jobCharger) {
		this.jobCharger = jobCharger;
	}
	public String getJobTeamCode() {
		return jobTeamCode;
	}
	public void setJobTeamCode(String jobTeamCode) {
		this.jobTeamCode = jobTeamCode;
	}
	public List<EmployeeVO> getJobAssistants() {
		return jobAssistants;
	}
	public void setJobAssistants(List<EmployeeVO> jobAssistants) {
		this.jobAssistants = jobAssistants;
	}
	
	
	
	
}
