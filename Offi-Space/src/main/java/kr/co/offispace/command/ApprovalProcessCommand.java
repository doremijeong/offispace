package kr.co.offispace.command;

public class ApprovalProcessCommand {
		
		private String empCode; 	// 결재자(합의자)		[APPROVAL]
		private String apvCode;		// 결재중인 문서 결재코드	[APPROVAL]
		private String apvRefuse; 	// 반려사유			[APPROVAL]
		private String apvMarkup;	// 결재문서 마크업		[APPROVAL]
		private String apvConfirm;	// 결재 승인여부		[APPROVAL_LINE]
		private int apvLineOrder;	// 내 결재 순번
		private String apvState;	// 결재상태(대기,진행,완료)
		
		public String getEmpCode() {
			return empCode;
		}
		public void setEmpCode(String empCode) {
			this.empCode = empCode;
		}
		public String getApvCode() {
			return apvCode;
		}
		public void setApvCode(String apvCode) {
			this.apvCode = apvCode;
		}
		public String getApvRefuse() {
			return apvRefuse;
		}
		public void setApvRefuse(String apvRefuse) {
			this.apvRefuse = apvRefuse;
		}
		public String getApvMarkup() {
			return apvMarkup;
		}
		public void setApvMarkup(String apvMarkup) {
			this.apvMarkup = apvMarkup;
		}
		public String getApvConfirm() {
			return apvConfirm;
		}
		public void setApvConfirm(String apvConfirm) {
			this.apvConfirm = apvConfirm;
		}
		public int getApvLineOrder() {
			return apvLineOrder;
		}
		public void setApvLineOrder(int apvLineOrder) {
			this.apvLineOrder = apvLineOrder;
		}
		public String getApvState() {
			return apvState;
		}
		public void setApvState(String apvState) {
			this.apvState = apvState;
		}
		
		
		
}
