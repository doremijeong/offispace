package kr.co.offispace.command;

import java.util.List;


public class JobcodeRegisterCommand {

	private String jobcodeCode;
	private String jobcodeName;
	private String jobCharger;
	private String jobTeamCode;
	private List<String> jobAssistants;

	public String getJobcodeCode() {
		return jobcodeCode;
	}
	public void setJobcodeCode(String jobcodeCode) {
		this.jobcodeCode = jobcodeCode;
	}
	public String getJobcodeName() {
		return jobcodeName;
	}
	public void setJobcodeName(String jobcodeName) {
		this.jobcodeName = jobcodeName;
	}
	public String getJobCharger() {
		return jobCharger;
	}
	public void setJobCharger(String jobCharger) {
		this.jobCharger = jobCharger;
	}
	public String getJobTeamCode() {
		return jobTeamCode;
	}
	public void setJobTeamCode(String jobTeamCode) {
		this.jobTeamCode = jobTeamCode;
	}
	public List<String> getJobAssistants() {
		return jobAssistants;
	}
	public void setJobAssistants(List<String> jobAssistants) {
		this.jobAssistants = jobAssistants;
	}
	@Override
	public String toString() {
		return "JobcodeRegisterCommand [jobcodeCode=" + jobcodeCode + ", jobcodeName=" + jobcodeName + ", jobCharger="
				+ jobCharger + ", jobTeamCode=" + jobTeamCode + ", jobAssistants=" + jobAssistants + "]";
	}




}
